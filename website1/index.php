  <h4>Here is the recipe that put <em class="tacobox">Tacos Tacos Tacos</em> on the map!</h4>
  <div class="mayan">
    <div class="row">
      <div class="col-lg-6 col-md-6 col-sm-12 col-xs-12">
        <img id="boring" title="Most Boring Taco in the World" alt="Most Boring Taco in the World" center src="assets/img/boring.jpg">
      </div>
      <div class="col-lg-6 col-md-6 col-sm-12 col-xs-12">
        <table class="table">
          <tr>
            <th>Most Boring Taco in the World</th>
          </tr>
          <tr>
            <th>Ingredients</th>
          </tr>
          <tr>
            <th>Seasoning</th>
            <th>Fillings/Toppings</th>
          </tr>
          <tr>
            <td>
              <ul>
                <li>2 tsp Hot Chili Powder</li>
                <li>1.5 tsp Paprika</li>
                <li>1 tsp Onion Powder</li>
                <li>0.5 tsp Garlic Powder</li>
                <li>0.5 tsp Ground Cumin</li>
                <li>0.5 tsp Oregano</li>
                <li>0.25 tsp Black Pepper</li>
                <li>0.5 tsp Cayenne</li>
                <li>0.5 tsp Red Pepper Flakes</li>
              </ul>
            </td>
            <td>
              <ul>
                <li>1 lb USDA Prime Ribeye</li>
                <li>8 Corn Tortillas</li>
                <li>1 medium Vidalia Onion</li>
                <li>1 Heart Romaine</li>
                <li>1 Can Black Olives</li>
                <li>1 Cup Shredded Cheddar Cheese</li>
                <li>2 Roma Tomatoes</li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Instructions</th>
          </tr>
          <tr>
            <td colspan="2">
              <ol>
                <li>Cut Steak into thin strips and place into hot saute pan with a small amount of vegetable oil. Cook until tender and brown.</li>
                <li>While steak is cooking: cut onion into thin slices, dice tomatoes, shred lettuce, drain olives. Put all of these ingredients into serving dishes.</li>
                <li>Mix all of the seasonings into a bowl. Be sure to combine them all very well. Once meat is fully cooked, add 0.5 cup of water and the seasoning mixture.  Still until seasoning has covered all of the meat. Add more water if needed.</li>
                <li>Warm tortillas and serve.</li>
              </ol>
